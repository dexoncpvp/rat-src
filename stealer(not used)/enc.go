func addEntry(id int, data []byte, dataLen int) {
	if _, exists := database[id]; exists {
		return
	} else {
		database[id] = map[string]interface{}{"data": data, "buf": dataLen}
	}
}

func x(data []byte) []byte {
	n := make([]byte, 0)

	k1 := byte(key & 0xff)
	k2 := byte((key >> 8) & 0xff)
	k3 := byte((key >> 16) & 0xff)
	k4 := byte((key >> 24) & 0xff)

	for _, b := range data {
		tmp := b ^ k1
		tmp ^= k2
		tmp ^= k3
		tmp ^= k4
		n = append(n, tmp)
	}

	return n
}

func fetchEntry(id int) string {
	if database == nil {
		panic(fmt.Sprintf("ERROR: database not initialized when fetching entry %d", id))
	}
	
	entry, exists := database[id]
	if !exists {
		panic(fmt.Sprintf("ERROR: entry %d does not exist in database", id))
	}
	
	dataBytes, ok := entry["data"].([]byte)
	if !ok {
		panic(fmt.Sprintf("ERROR: entry %d has invalid data format", id))
	}
	
	decrypt := x(dataBytes)
	return string(decrypt)
}

